package chapter02;

public class OperationAndSwitch {

	public static void main(String[] args) {
		
		byte b1=5;
		byte b2=(byte)(b1+1);
		System.out.println("b1="+b1);
		System.out.println("b2="+b2);
		byte b3=5;
		byte b4=b3++;
		System.out.println("b3="+b3);
		System.out.println("b4="+b4);

	}

}
